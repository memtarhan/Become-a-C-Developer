#include <cstdio>

int main()
{
    int number; // If not assigned it's 0 by default
    float decimal; // If not assigned it's 0.0000... by default
    bool boolean; // If not assigned it's false by default

    printf("Number: %d\n", number);
    printf("Decimal: %f\n", decimal);
    printf("Boolean: %d\n", boolean);

    return 0;
}